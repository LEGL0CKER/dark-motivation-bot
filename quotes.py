def get_quote(config: dict) -> str:
    """Return a random quote. Uses curated list by default, ZenQuotes as fallback."""
    if config.get('use_curated_quotes', True):
        return random.choice(DARK_MOTIVATION_QUOTES)

    # ZenQuotes API fallback
    try:
        resp = requests.get('https://zenquotes.io/api/random', timeout=10)
        resp.raise_for_status()
        data = resp.json()[0]
        return f"{data['q'].upper()} — {data['a'].upper()}"
    except Exception:
        return random.choice(DARK_MOTIVATION_QUOTES)
